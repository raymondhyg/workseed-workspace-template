def fail(message: str) -> None:
    print(f"[FAIL] {message}")
    raise SystemExit(1)


def has_chinese_summary_immediately_after_title(text: str) -> bool:
    lines = text.splitlines()
    if not lines or not lines[0].startswith("# "):
        return False
    for line in lines[1:]:
        if not line.strip():
            continue
        return line.strip() == "## 中文发布摘要"
    return False


def main() -> int:
    for rel in REQUIRED_PATHS:
        if not (ROOT / rel).exists():
            fail(f"missing required path: {rel}")

    for rel, snippets in REQUIRED_SNIPPETS.items():
        text = (ROOT / rel).read_text(encoding="utf-8")
        for snippet in snippets:
            if snippet not in text:
                fail(f"{rel} missing text: {snippet}")

    for rel in CHINESE_FIRST_RELEASE_NOTES:
        text = (ROOT / rel).read_text(encoding="utf-8")
        if not has_chinese_summary_immediately_after_title(text):
            fail(f"{rel} must place Chinese summary immediately after title")

    load_records = sorted((ROOT / "workspace-records" / "capability-loads").glob("*-capability-load.md"))
    for record in load_records:
        text = record.read_text(encoding="utf-8")
        for snippet in [
            "## Installed Runtime Surfaces",
            "## Missing Runtime Surfaces",
            "## Skipped Extra Runtime Surfaces",
            "## Runtime Availability",
            "Runtime availability: `",
        ]:
            if snippet not in text:
                fail(f"{record.relative_to(ROOT)} missing post-install field: {snippet}")

        required_installed = [
            ".codex/agents/mark.toml",
            ".codex/agents/mira.toml",
            ".codex/agents/prism.toml",
            ".codex/agents/system-maintainer.toml",
            ".codex/agents/workflow-coach.toml",
            ".agents/skills/b2b-strategy/SKILL.md",
            ".agents/skills/background-memory/SKILL.md",
            ".agents/skills/ecommerce-domain/SKILL.md",
            ".agents/skills/image-production/SKILL.md",
            ".agents/skills/prism-writeback/SKILL.md",
            ".agents/skills/reference-learning/SKILL.md",
            ".agents/skills/workflow-coach/SKILL.md",
            "docs/codex/prism-knowledge-writeback.md",
            "docs/capability-packs/b2b-strategy.md",
            "docs/capability-packs/image-production.md",
            "docs/core/p1/core-p1-prism-writeback-operating-loop-2026-07-03.md",
            "templates/prism-decision-template.md",
            "templates/mark-mira-prism-handoff-template.md",
        ]
        for rel in required_installed:
            if not (ROOT / rel).exists():
                fail(f"post-install record exists but required runtime surface is missing: {rel}")

    for path in ROOT.rglob("*"):
        if not path.is_file() or ".git" in path.parts:
            continue
        if path == Path(__file__).resolve():
            continue
        if path.suffix.lower() not in {".md", ".py", ".txt", ".toml", ".json", ".yaml", ".yml"}:
            continue
        text = path.read_text(encoding="utf-8")
        for snippet in FORBIDDEN_SNIPPETS:
            if snippet in text:
                fail(f"{path.relative_to(ROOT)} contains forbidden claim: {snippet}")

    print("[PASS] Workspace deployment checks passed")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
